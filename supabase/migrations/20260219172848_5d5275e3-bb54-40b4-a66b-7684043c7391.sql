CREATE TABLE public.waqf_bylaws (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  part_number integer NOT NULL,
  chapter_number integer,
  part_title text NOT NULL,
  chapter_title text,
  content text NOT NULL DEFAULT '',
  sort_order integer NOT NULL DEFAULT 0,
  is_visible boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.waqf_bylaws ENABLE ROW LEVEL SECURITY;

-- Admin full access
CREATE POLICY "Admins can manage bylaws"
ON public.waqf_bylaws FOR ALL
USING (has_role(auth.uid(), 'admin'::app_role));

-- Beneficiaries and waqif read-only
CREATE POLICY "Authorized roles can view bylaws"
ON public.waqf_bylaws FOR SELECT
USING (
  has_role(auth.uid(), 'admin'::app_role) OR
  has_role(auth.uid(), 'beneficiary'::app_role) OR
  has_role(auth.uid(), 'waqif'::app_role)
);

-- Auto-update updated_at
CREATE TRIGGER update_waqf_bylaws_updated_at
BEFORE UPDATE ON public.waqf_bylaws
FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- Audit log trigger
CREATE TRIGGER audit_waqf_bylaws
AFTER INSERT OR UPDATE OR DELETE ON public.waqf_bylaws
FOR EACH ROW EXECUTE FUNCTION public.audit_trigger_func();

